#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	i;
	int	j;

	j = size - 1;
	i = 0;
	while (i < (size / 2))
	{
		a = tab[i];
		tab[i] = tab[j];
		tab[j] = a;
		i++;
		j--;
	}
}
